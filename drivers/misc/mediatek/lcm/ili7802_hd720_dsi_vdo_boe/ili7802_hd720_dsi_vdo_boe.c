    data_array[1]=0x0278FFFF; 
    data_array[2]=0x00000100;
    dsi_set_cmdq(data_array, 3, 1); 
    MDELAY(10);

	data_array[0] = 0x00013700;
	dsi_set_cmdq(data_array, 1, 1);
	read_reg_v2(0x00, &buffer[0], 1);
	
	data_array[0] = 0x00013700;
	dsi_set_cmdq(data_array, 1, 1);
	read_reg_v2(0x01, &buffer[1], 1);
	
	id = (buffer[0] << 8) | buffer[1];
	
