static void lcm_init(void)
{
	lcd_power_en(1);
	MDELAY(50);
	lcm_gpio_output(0,0);
	MDELAY(10);
	lcm_gpio_output(0,1);
	MDELAY(50);      
	push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);  
}


static void lcm_suspend(void)
{	
    push_table(lcm_sleep_mode_in_setting, sizeof(lcm_sleep_mode_in_setting) / sizeof(struct LCM_setting_table), 1);
}
static void lcm_resume(void)
{
    //push_table(lcm_sleep_mode_out_setting, sizeof(lcm_sleep_mode_out_setting) / sizeof(struct LCM_setting_table), 1);
    SET_RESET_PIN(1);
	MDELAY(20);
    SET_RESET_PIN(0);
    MDELAY(20);
    SET_RESET_PIN(1);
    MDELAY(120);
	
    push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);  
}

static unsigned int lcm_compare_id(void)
{
	unsigned int id = 0;
	unsigned char buffer[0];
	unsigned int array[16];
	
	//Do reset here
	SET_RESET_PIN(1);
	SET_RESET_PIN(0);
	MDELAY(25);

	SET_RESET_PIN(1);
	MDELAY(50);      

	array[0] = 0x00023700;
	dsi_set_cmdq(array, 1, 1);
	
	read_reg_v2(0xDB, buffer, 1);
	id = buffer[0];

 if (id==0x80){
        array[0]=0x01FE1500;
	    dsi_set_cmdq(array,1, 1);

	    array[0] = 0x00013700;
	    dsi_set_cmdq(array, 1, 1);
	
	    read_reg_v2(0xde, buffer, 1);
	    id_high = buffer[0];
	    read_reg_v2(0xdf, buffer, 1);
	    id_low = buffer[0];
	
	    id = (id_high<<8) | id_low;
    }

	#if defined(BUILD_LK)
	printf("NT35521 uboot %s id = 0x%x \n", __func__, id);
	#else
	printk("NT35521 kernel %s id = 0x%x \n", __func__, id);
	#endif
	
	return (0x3552 == id) ? 1 : 0;
}

